#!/bin/bash

NUMOFTHREADS=12
OMP_NUM_THREADS=$NUMOFTHREADS ./Rgemm.dd -NOCHECK -NN > log.Rgemm.dd.$NUMOFTHREADS.NN
OMP_NUM_THREADS=$NUMOFTHREADS ./Rgemm.dd -NOCHECK -NT > log.Rgemm.dd.$NUMOFTHREADS.NT
OMP_NUM_THREADS=$NUMOFTHREADS ./Rgemm.dd -NOCHECK -TN > log.Rgemm.dd.$NUMOFTHREADS.TN
OMP_NUM_THREADS=$NUMOFTHREADS ./Rgemm.dd -NOCHECK -TT > log.Rgemm.dd.$NUMOFTHREADS.TT

OMP_NUM_THREADS=$NUMOFTHREADS ./Rgemm.dd -NOCHECK -NN -M0 50 -N0 100 -K0 200 -STEPM 1 -STEPN 3 -STEPK 10 > log.Rgemm.dd.$NUMOFTHREADS.NN.050.100.200.01.03.10
OMP_NUM_THREADS=$NUMOFTHREADS ./Rgemm.dd -NOCHECK -NT -M0 50 -N0 100 -K0 200 -STEPM 1 -STEPN 3 -STEPK 10 > log.Rgemm.dd.$NUMOFTHREADS.NT.050.100.200.01.03.10
OMP_NUM_THREADS=$NUMOFTHREADS ./Rgemm.dd -NOCHECK -TN -M0 50 -N0 100 -K0 200 -STEPM 1 -STEPN 3 -STEPK 10 > log.Rgemm.dd.$NUMOFTHREADS.TN.050.100.200.01.03.10
OMP_NUM_THREADS=$NUMOFTHREADS ./Rgemm.dd -NOCHECK -TT -M0 50 -N0 100 -K0 200 -STEPM 1 -STEPN 3 -STEPK 10 > log.Rgemm.dd.$NUMOFTHREADS.TT.050.100.200.01.03.10

exit
